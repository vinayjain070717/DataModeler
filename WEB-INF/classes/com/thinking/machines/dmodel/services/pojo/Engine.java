package com.thinking.machines.dmodel.services.pojo;
public class Engine
{
private Integer code;
private String name;
public Engine()
{
this.code=null;
this.name=null;
}
public void setCode(Integer code)
{
this.code=code;
}
public Integer getCode()
{
return this.code;
}
public void setName(String name)
{
this.name=name;
}
public String getName()
{
return this.name;
}
}
